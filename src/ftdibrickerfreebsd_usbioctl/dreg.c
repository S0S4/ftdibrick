int writecell(int fd, int num, u_int16_t val) {
  struct usb_ctl_request request = {0};
  int res;

  request.ucr_addr = 0;
  request.ucr_request.bmRequestType = 0x40;
  request.ucr_request.bRequest = 0x91;
  request.ucr_request.wValue[0] = val & 0xff;
  request.ucr_request.wValue[1] = val >> 8;
  request.ucr_request.wIndex[0] = num & 0xff;
  request.ucr_request.wIndex[1] = num >> 8;
  request.ucr_request.wLength[0] = 0x00;
  request.ucr_request.wLength[1] = 0x00;
  request.ucr_data = NULL;
  request.ucr_flags = 0;
  request.ucr_actlen = 0;
  res = ioctl(fd, USB_DO_REQUEST, &request);
  if (res < 0) {
    fprintf(stderr, "failed to write value %d 0x%X into cell %d %s\n", val, val,
            num, strerror(errno));
  }

  return res;
}

